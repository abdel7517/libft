#include "libft.h"

size_t	ft_strlen(char *str)
{
	size_t		i;
	size_t		q;
	char		end;

	q = 1;
	i = 0;
	while (q > 0)
	{
		end = str[i];
		if (end == '\0')
		{
			q = 0;
			return (i);
		}
		i++;
	}
	return (0);
}
